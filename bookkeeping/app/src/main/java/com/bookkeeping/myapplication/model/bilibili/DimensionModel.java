public class DimensionModel {
    /**
     * width : 3840
     * height : 2160
     * rotate : 0
     */

    private int width;
    private int height;
    private int rotate;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getRotate() {
        return rotate;
    }

    public void setRotate(int rotate) {
        this.rotate = rotate;
    }
}
